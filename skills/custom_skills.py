def search_web(args: dict) -> dict:
    """Search DuckDuckGo (no API key) and return top results."""
    import requests
    from urllib.parse import quote
    query = args.get("query", "")
    try:
        resp = requests.get(
            f"https://api.duckduckgo.com/?q={quote(query)}&format=json&no_redirect=1",
            timeout=8,
            headers={"User-Agent": "NOVA/1.0"}
        )
        data = resp.json()
        results = []
        if data.get("AbstractText"):
            results.append({"title": "Summary", "text": data["AbstractText"][:300]})
        for r in data.get("RelatedTopics", [])[:3]:
            if isinstance(r, dict) and r.get("Text"):
                results.append({"title": r.get("FirstURL", ""), "text": r["Text"][:200]})
        return {"query": query, "results": results}
    except Exception as e:
        return {"error": str(e)}

register_skill("search_web", "Search the web via DuckDuckGo", search_web)


