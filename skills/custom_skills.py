def get_weather(args: dict) -> dict:
    """Get current weather for a city using wttr.in (no API key needed)."""
    import requests
    city = args.get("city", "Amsterdam")
    try:
        resp = requests.get(
            f"https://wttr.in/{city}?format=j1",
            timeout=5
        )
        data = resp.json()
        current = data["current_condition"][0]
        return {
            "city": city,
            "temp_c": current["temp_C"],
            "feels_like_c": current["FeelsLikeC"],
            "description": current["weatherDesc"][0]["value"],
            "humidity": current["humidity"],
            "wind_kmph": current["windspeedKmph"]
        }
    except Exception as e:
        return {"error": str(e)}

register_skill("get_weather", "Get current weather for a city", get_weather)


def calculate(args: dict) -> dict:
    """Safe math expression evaluator."""
    import math
    expr = args.get("expression", "")
    try:
        # Safe eval with math functions available
        allowed = {k: getattr(math, k) for k in dir(math) if not k.startswith("_")}
        allowed.update({"abs": abs, "round": round, "min": min, "max": max, "sum": sum})
        result = eval(expr, {"__builtins__": {}}, allowed)
        return {"expression": expr, "result": result}
    except Exception as e:
        return {"error": str(e), "expression": expr}

