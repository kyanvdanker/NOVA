def unit_convert(args: dict) -> dict:
    """Convert between common units."""
    value = float(args.get("value", 0))
    from_unit = args.get("from", "").lower()
    to_unit = args.get("to", "").lower()
    
    conversions = {
        ("km", "miles"): lambda x: x * 0.621371,
        ("miles", "km"): lambda x: x * 1.60934,
        ("kg", "lbs"): lambda x: x * 2.20462,
        ("lbs", "kg"): lambda x: x * 0.453592,
        ("c", "f"): lambda x: x * 9/5 + 32,
        ("f", "c"): lambda x: (x - 32) * 5/9,
        ("m", "ft"): lambda x: x * 3.28084,
        ("ft", "m"): lambda x: x * 0.3048,
        ("l", "gal"): lambda x: x * 0.264172,
        ("gal", "l"): lambda x: x * 3.78541,
    }
    
    key = (from_unit, to_unit)
    if key in conversions:
        result = conversions[key](value)
        return {"value": value, "from": from_unit, "to": to_unit, "result": round(result, 4)}
    return {"error": f"Conversion {from_unit} → {to_unit} not supported"}

register_skill("unit_convert", "Convert between units", unit_convert)

